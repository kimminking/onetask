def run():
    with engine.connect() as conn:
        # 현재 최대 id
        max_id = conn.execute(text("SELECT COALESCE(MAX(id), 0) FROM words")).scalar()

        inserted = 0
        for level, words in [(1, HSK1), (2, HSK2)]:
            existing = {r[0] for r in conn.execute(
                text("SELECT chinese FROM words WHERE hsk_level=:l"), {"l": level}).fetchall()}
            new_words = [(ch, py, mn) for ch, py, mn in words if ch not in existing]
            if not new_words:
                print(f"HSK {level}: 이미 모두 존재")
                continue
            conn.execute(text(
                "INSERT INTO words (chinese, pinyin, meaning, hsk_level) "
                "VALUES (:c, :p, :m, :l)"
            ), [{"c": c, "p": p, "m": m, "l": level} for c, p, m in new_words])
            conn.commit()
            print(f"HSK {level}: {len(new_words)}개 삽입")
            inserted += len(new_words)

        total1 = conn.execute(text("SELECT COUNT(*) FROM words WHERE hsk_level=1")).scalar()
        total2 = conn.execute(text("SELECT COUNT(*) FROM words WHERE hsk_level=2")).scalar()
        print(f"\nHSK 1: {total1}개 / HSK 2: {total2}개")


if __name__ == "__main__":
    run()
